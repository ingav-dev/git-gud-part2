def multiply(num1:int, num2:int, num3:int) -> int:
    ''' Add multiplication function '''
    return num1 * num2 * num3


def add(num1:int, num2:int) -> int:
    ''' Add addition function '''
    return num1 + num2

def subtract(num1:int, num2:int) -> int:
    ''' Add subtraction function '''
    return num1 - num2
